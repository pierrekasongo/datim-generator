package dhis2datim;

/**
 *
 * @author ebongo
 */
public class OrgUnit {
    
    private String nom;
    private String code;
    
    public OrgUnit(String _nom,String _code){
        this.nom=_nom;
        this.code=_code;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }
    
}
